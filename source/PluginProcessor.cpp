bool PluginProcessor::isBusesLayoutSupported (const BusesLayout& layouts) const
{
  #if JucePlugin_IsMidiEffect
    juce::ignoreUnused (layouts);
    return true;
  #else
    // This is the place where you check if the layout is supported.
    // In this template code we only support mono or stereo.
    if (layouts.getMainOutputChannelSet() != juce::AudioChannelSet::mono()
     && layouts.getMainOutputChannelSet() != juce::AudioChannelSet::stereo())
        return false;

    // This checks if the input layout matches the output layout
   #if ! JucePlugin_IsSynth
    if (layouts.getMainOutputChannelSet() != layouts.getMainInputChannelSet())
        return false;
   #endif

    return true;
  #endif
}

void PluginProcessor::processBlock (juce::AudioBuffer<float>& buffer,
                                              juce::MidiBuffer& midiMessages)
{
    const float gain = *gainParam;
    const float drive = *driveParam;
    const float tone = *toneParam;

    auto numChannels = buffer.getNumChannels();
    auto numSamples = buffer.getNumSamples();

    // update the filter coefficients (low-pass filter after distortion)
    *toneFilter.state = *juce::dsp::IIR::Coefficients<float>::makeLowPass(getSampleRate(), tone);

    juce::dsp::AudioBlock<float> block(buffer);

    for (int ch = 0; ch < buffer.getNumChannels(); ++ch)
    {
        auto* channelData = buffer.getWritePointer(ch);
        for (int sa = 0; sa < buffer.getNumSamples(); ++sa)
        {
            float in = channelData[sa];
            float distort = std::tanh(drive * in); // controls how aggressive distortion is
            channelData[sa] = gain * distort;         // gain controls output level
        }
    }

    // apply the tone filter
    toneFilter.process(juce::dsp::ProcessContextReplacing(block));

}

